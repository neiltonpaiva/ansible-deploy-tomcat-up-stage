        success "Rollback executado"
        echo "$(date): ROLLBACK executed via deploy-manager" >> "$FSX_MOUNT/logs/ubuntu-deploys.log"
    else
        error "Rollback falhou - verifica√ß√£o manual necess√°ria"
    fi
